uint8_t crc8(const uint8_t *data, uint8_t len) {
    uint8_t crc = 0x00U;
    for (uint8_t i = 0; i < len; i++) {
        crc = crc8_table[crc ^ data[i]];
    }
    return crc;
}

void uart_init(void) {

    /* ── Enable USART2 clock on APB1 ──────────────────────────────────────
     * GPIO clocks (PA2/PA3 AF7) are already enabled in gpio_init(). */
    RCC->APB1ENR |= RCC_APB1ENR_USART2EN;
    (void)RCC->APB1ENR;   /* Dummy read to flush APB write buffer */

    /* ── BRR: baud rate divisor ────────────────────────────────────────────
     * Must be set while UE=0; changing BRR while enabled causes framing errors. */
    USART2->BRR = USART2_BRR_VAL;   /* 0x16D = 365 → 115200 baud at 42 MHz */

    /* ── CR1: 8N1, enable TX + RX + USART ─────────────────────────────────
     * Default CR1 gives 8 data bits, 1 stop bit, no parity.
     * No interrupts needed — we poll TXE for transmission. */
    USART2->CR1 = USART_CR1_TE      /* Transmitter enable — idles line high */
                | USART_CR1_RE      /* Receiver enable */
                | USART_CR1_UE;     /* USART enable — starts baud generation */
}

void uart_send_byte(uint8_t b) {
    /* Wait until TDR is empty — blocking, but frames are short (6 bytes) */
    while (!(USART2->SR & USART_SR_TXE));
    USART2->DR = b;   /* Writing DR clears TXE automatically */
}

static const char *state_name(uint8_t state) {
    switch (state) {
        case 0:  return "IDLE";
        case 1:  return "RUNNING";
        case 2:  return "FAULT";
        default: return "UNKNOWN";
    }
}

static void uart_send_str(const char *s) {
    while (*s) uart_send_byte((uint8_t)*s++);
}

static void uart_print_num(uint32_t n) {
    char buf[10];
    int8_t i = 0;
    if (n == 0) { uart_send_byte('0'); return; }
    while (n > 0) { buf[i++] = (char)('0' + (n % 10)); n /= 10; }
    while (--i >= 0) uart_send_byte((uint8_t)buf[i]);
}

void uart_send_frame(uint16_t rpm, uint8_t duty, uint8_t state) {
    /* Build ASCII frame: "RPM:1500 DUTY:50% STATE:RUNNING CRC:xx\r\n" */
    uint8_t raw[5];
    raw[0] = 0xAAU;
    raw[1] = (uint8_t)(rpm >> 8);
    raw[2] = (uint8_t)(rpm & 0xFFU);
    raw[3] = duty;
    raw[4] = state;
    uint8_t crc = crc8(raw, 5U);

    uart_send_str("RPM:");
    uart_print_num(rpm);
    uart_send_str(" DUTY:");
    uart_print_num(duty);
    uart_send_byte('%');
    uart_send_str(" STATE:");
    uart_send_str(state_name(state));
    uart_send_str(" CRC:");
    uart_print_num(crc);
    uart_send_str("\r\n");

    while (!(USART2->SR & USART_SR_TC));
}
