/**
 * uart.c — USART2 debug interface at 115200 baud
 *
 * USART2 sits on APB1 at 42 MHz.
 * BRR calculation (RM0090 §30.3.4):
 *   USARTDIV = f_PCLK / (16 × baud) = 42000000 / (16 × 115200) = 22.786...
 *   Mantissa = 22 = 0x16, Fraction = round(0.786 × 16) = 13 = 0xD
 *   BRR = (22 << 4) | 13 = 0x16D = 365
 *
 * Frame format (binary, 9 bytes total):
 *   [0]    0xAA  start marker
 *   [1:2]  RPM   uint16_t big-endian
 *   [3]    duty  uint8_t  0–100
 *   [4]    state uint8_t  0=IDLE 1=RUNNING 2=FAULT
 *   [5]    CRC8  over bytes [0–4]
 *
 * CRC-8/MAXIM (polynomial 0x31, reflected) is used — common in Dallas/Maxim
 * sensors and easy to verify with a lookup table.
 */

